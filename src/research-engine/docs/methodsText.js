/**
 * methodsText.js — prompt42 Task 8. Pure, network-free generator for the Methods
 * section that ships inside the one-click journal-submission package.
 *
 * It does NOT invent data: every sentence is built from a context object the caller
 * assembles from the project's ACTUAL settings + the live meta-analysis result
 * (model, measure, heterogeneity, k, software version, GRADE). Where a field is
 * missing it is omitted or marked "[not recorded]" rather than fabricated — the
 * researcher fills those gaps. Statistical-method statements describe what the
 * engine ACTUALLY ran (inverse-variance fixed effect; DerSimonian–Laird random
 * effects; optional Hartung–Knapp–Sidik–Jonkman), mirroring the report footer.
 *
 * Pure + exported for unit tests (tests/unit/methodsText.test.js).
 */

const PLACEHOLDER = '[not recorded — please complete]';

function clean(s) { return String(s == null ? '' : s).trim(); }
function line(parts) { return parts.filter(Boolean).join(''); }

/**
 * @param {object} ctx
 *   projectName, generatedAt(string)
 *   pico {question,P,I,C,O}
 *   databases(string), dateSearched(string), registration(string)
 *   prisma {identified,deduped,screened,excludedFullText,included}
 *   screening {reviewers(number), blind(bool), conflictResolution(string)}
 *   measure(string label), model('fixed'|'random'), hksj(bool)
 *   k(number), heterogeneity {I2,tau2,Q,Qdf,Qp}, predInterval(bool)
 *   outcomes(string[]), robTool(string), grade(bool), software(string)
 * @returns {string} GitHub-flavoured Markdown
 */
export function buildMethodsMarkdown(ctx = {}) {
  const c = ctx || {};
  const pico = c.pico || {};
  const het = c.heterogeneity || {};
  const prisma = c.prisma || {};
  const screening = c.screening || {};
  const out = [];

  out.push(`# Methods — ${clean(c.projectName) || 'Systematic review and meta-analysis'}`);
  out.push('');
  out.push(`_Auto-generated by META·LAB${c.software ? ` (${clean(c.software)})` : ''}${c.generatedAt ? ` on ${clean(c.generatedAt)}` : ''}. Review and edit before submission; bracketed placeholders need your input._`);
  out.push('');

  // ── Protocol & registration ────────────────────────────────────────────────
  out.push('## Protocol and registration');
  out.push(c.registration
    ? `This review was registered/recorded as: ${clean(c.registration)}.`
    : `The review protocol registration (e.g. PROSPERO) is ${PLACEHOLDER}.`);
  out.push('');

  // ── Eligibility (PICO) ──────────────────────────────────────────────────────
  out.push('## Eligibility criteria (PICO)');
  if (pico.question) out.push(`**Review question.** ${clean(pico.question)}`);
  out.push(`- **Population:** ${clean(pico.P) || PLACEHOLDER}`);
  out.push(`- **Intervention/Exposure:** ${clean(pico.I) || PLACEHOLDER}`);
  out.push(`- **Comparator:** ${clean(pico.C) || PLACEHOLDER}`);
  out.push(`- **Outcomes:** ${clean(pico.O) || PLACEHOLDER}`);
  out.push('');

  // ── Information sources & search ────────────────────────────────────────────
  out.push('## Information sources and search strategy');
  out.push(line([
    'We searched ',
    c.databases ? clean(c.databases) : PLACEHOLDER,
    c.dateSearched ? ` (last searched ${clean(c.dateSearched)})` : '',
    '. The full search strategy for at least one database is reported in the search-strategy export.',
  ]));
  out.push('');

  // ── Study selection / screening ─────────────────────────────────────────────
  out.push('## Study selection');
  const revN = Number(screening.reviewers);
  out.push(line([
    'Records were de-duplicated and screened ',
    Number.isFinite(revN) && revN >= 2 ? `by ${revN} independent reviewers` : 'by independent reviewers',
    screening.blind ? ', blinded to each other\'s decisions' : '',
    ', first on title/abstract and then on full text against the eligibility criteria. ',
    screening.conflictResolution ? `Disagreements were resolved by ${clean(screening.conflictResolution)}. ` : 'Disagreements were resolved by discussion. ',
    'The selection process is summarised in the PRISMA 2020 flow diagram included in this package.',
  ]));
  if (prisma.identified != null || prisma.included != null) {
    out.push('');
    out.push(line([
      'In total, ',
      prisma.identified != null ? `${prisma.identified} records were identified` : 'records were identified',
      prisma.deduped != null ? `, ${prisma.deduped} duplicates removed` : '',
      prisma.included != null ? `, and ${prisma.included} studies were included in the review.` : '.',
    ]));
  }
  out.push('');

  // ── Data extraction ─────────────────────────────────────────────────────────
  out.push('## Data extraction');
  out.push('Study-level data (design, population, intervention/comparator, outcomes, sample sizes and effect estimates with measures of variance) were extracted into a structured form; raw inputs were retained so every derived value is auditable. The extracted study table is included in this package.');
  out.push('');

  // ── Risk of bias ────────────────────────────────────────────────────────────
  out.push('## Risk of bias');
  out.push(c.robTool
    ? `Risk of bias was assessed at the outcome level using ${clean(c.robTool)}; each domain judgement was recorded with its supporting rationale.`
    : `Risk of bias assessment tool: ${PLACEHOLDER} (e.g. Cochrane RoB 2 for randomised trials).`);
  out.push('');

  // ── Statistical synthesis ───────────────────────────────────────────────────
  out.push('## Statistical analysis');
  const modelLabel = c.model === 'fixed'
    ? 'an inverse-variance common (fixed) effect model'
    : 'a DerSimonian–Laird random-effects model';
  out.push(line([
    'Effect sizes',
    c.measure ? ` (${clean(c.measure)})` : '',
    ' were pooled using ',
    modelLabel,
    c.hksj ? ', with the Hartung–Knapp–Sidik–Jonkman adjustment to the confidence interval' : '',
    '. ',
    Number.isFinite(Number(c.k)) ? `A total of ${c.k} studies contributed to the primary synthesis. ` : '',
    'Ratio measures were pooled on the log scale and back-transformed for presentation.',
  ]));
  out.push('');
  out.push(line([
    'Heterogeneity was quantified with the I² statistic and τ² (between-study variance), and tested with Cochran\'s Q. ',
    het.I2 != null ? `For the primary outcome, I² = ${het.I2}%` : '',
    het.tau2 != null ? `, τ² = ${het.tau2}` : '',
    (het.Q != null) ? `, Q = ${het.Q}${het.Qdf != null ? ` (df = ${het.Qdf})` : ''}${het.Qp != null ? `, p = ${het.Qp}` : ''}` : '',
    (het.I2 != null || het.Q != null) ? '. ' : '',
    c.predInterval ? 'A 95% prediction interval is reported for the random-effects estimate. ' : '',
  ]).trim() || 'Heterogeneity was quantified with I² and τ² and tested with Cochran\'s Q.');
  out.push('');
  if (Array.isArray(c.outcomes) && c.outcomes.length) {
    out.push(`A separate forest plot was produced for each outcome: ${c.outcomes.map(clean).filter(Boolean).join('; ')}.`);
    out.push('');
  }

  // ── Certainty (GRADE) ───────────────────────────────────────────────────────
  out.push('## Certainty of evidence');
  out.push(c.grade
    ? 'The certainty of evidence for each outcome was rated using the GRADE approach (risk of bias, inconsistency, indirectness, imprecision and publication bias).'
    : `Certainty of evidence (GRADE) assessment: ${PLACEHOLDER}.`);
  out.push('');

  // ── Software ────────────────────────────────────────────────────────────────
  out.push('## Software');
  out.push(line([
    'Screening, data extraction, meta-analysis and figures were produced in META·LAB',
    c.software ? ` (${clean(c.software)})` : '',
    '. Statistical methods implemented: inverse-variance fixed effect and DerSimonian–Laird random effects',
    c.hksj ? ', with Hartung–Knapp–Sidik–Jonkman adjustment' : '',
    '. Verify all numbers against your primary analysis before submission.',
  ]));
  out.push('');

  return out.join('\n');
}

export default buildMethodsMarkdown;
