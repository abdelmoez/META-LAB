/**
 * rob2.js — the Cochrane RoB 2 instrument for parallel-group randomised trials,
 * "effect of ASSIGNMENT to intervention" (intention-to-treat) variant.
 *
 * PURE: data + deterministic algorithm. No Prisma / Express / React / network /
 * randomness / Date.now(). Same input → same output (this is what makes it
 * testable and citable).
 *
 * The exported `ROB2` object is JSON-SERIALISABLE instrument DATA (domains,
 * signalling questions, response options, guidance, declarative branching) so
 * `GET /api/rob/instruments/rob2` can ship it to a data-driven UI. The judgement
 * ALGORITHMS are exported as separate pure functions (`judgeDomain`,
 * `judgeOverall`) which the generic engine (engine.js) calls.
 *
 * SOURCE OF THE ALGORITHM (see src/research-engine/docs/rob-validation.md):
 *   - Sterne et al., RoB 2 guidance. Domain 1 (Table 4, randomisation) and the
 *     Overall roll-up (Table 1) are encoded VERBATIM from the official guidance
 *     decision tables. Domains 2–5 encode the CURRENT (22 Aug 2019) RoB 2 tool
 *     algorithm whose signalling-question structure (2.1–2.7, 3.1–3.4, 4.1–4.5,
 *     5.1–5.3) the build spec and the live riskofbias.info Excel tool use.
 *   - Every branch is covered by a golden test in tests/unit/rob2-*.test.js.
 *
 * The engine COMPUTES the proposed judgement and returns a human-readable
 * `reasons` trace; the human decides and may override with a logged rationale.
 */

